package edu.kit.joana.ui.ifc.sdg.gui.contextmenu;


import org.eclipse.core.resources.IMarker;
import org.eclipse.jface.action.IAction;
import org.eclipse.jface.viewers.ISelection;
import org.eclipse.ui.IActionDelegate;
import org.eclipse.ui.IEditorActionDelegate;
import org.eclipse.ui.IEditorPart;

import edu.kit.joana.ui.ifc.sdg.gui.actions.MarkerAction;

public abstract class MarkerActionDelegate implements IEditorActionDelegate {
    protected MarkerAction concreteAction;

    /**
     * @see IActionDelegate#run(IAction)
     */
    public void run(IAction action) {
    	concreteAction.run();
    }

    public IMarker getMarker() {
    	return concreteAction.getMarker();
    }

    /* (non-Javadoc)
     * @see org.eclipse.ui.IEditorActionDelegate#setActiveEditor(org.eclipse.jface.action.IAction, org.eclipse.ui.IEditorPart)
     */
    public void setActiveEditor(IAction action, IEditorPart targetEditor) { }


	/* (non-Javadoc)
	 * @see org.eclipse.ui.IActionDelegate#selectionChanged(org.eclipse.jface.action.IAction, org.eclipse.jface.viewers.ISelection)
	 */
	public void selectionChanged(IAction action, ISelection selection) { }

}

